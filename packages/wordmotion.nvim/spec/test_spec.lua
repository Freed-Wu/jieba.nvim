package.path = package.path .. ';lua/?.lua'

local fs = require 'vim.fs'
local txt = fs.joinpath(fs.dirname(debug.getinfo(1).source:match("@?(.*)")), "test.txt")

-- luacheck: ignore 113
---@diagnostic disable: undefined-global
describe("test cursor", function()
    local Cursor = require "wordmotion".Cursor
    local cursor = Cursor:from_path(txt)
    it("tests from path", function()
        assert.are.equal(#cursor.lines, 3)
    end)
    it("tests utf8", function()
        local pos = cursor:get_character(false, { 2, 0 })
        assert.are.equal(pos[1], 1)
        assert.are.equal(pos[2], 12)
        pos = cursor:get_character(true, { 2, 15 })
        assert.are.equal(pos[1], 3)
        assert.are.equal(pos[2], 0)
        pos = cursor:get_character(false, { 1, 0 })
        assert.are.equal(pos[1], 1)
        assert.are.equal(pos[2], 0)
        pos = cursor:get_character(true, { 3, 24 })
        assert.are.equal(pos[1], 3)
        assert.are.equal(pos[2], 24)
    end)
end)

describe("test ^/$", function()
    local Cursor = require "wordmotion.line".Cursor
    local cursor = Cursor:from_path(txt)
    it("tests ^", function()
        local pos = cursor:get_position(-1, true, { 2, 3 })
        assert.are.equal(pos[1], 2)
        assert.are.equal(pos[2], 0)
        pos = cursor:get_position(-1, true, pos)
        assert.are.equal(pos[1], 2)
        assert.are.equal(pos[2], 0)
    end)
    it("tests $", function()
        local pos = cursor:get_position(1, false, { 2, 3 })
        assert.are.equal(pos[1], 2)
        assert.are.equal(pos[2], 15)
        pos = cursor:get_position(1, false, pos)
        assert.are.equal(pos[1], 2)
        assert.are.equal(pos[2], 15)
    end)
end)

describe("test h/l", function()
    local Cursor = require "wordmotion.char".Cursor
    local cursor = Cursor:from_path(txt)
    it("tests h", function()
        local pos = cursor:get_position(-2, true, { 2, 6 })
        assert.are.equal(pos[1], 2)
        assert.are.equal(pos[2], 0)
    end)
    it("tests l", function()
        local pos = cursor:get_position(3, true, { 2, 6 })
        assert.are.equal(pos[1], 2)
        assert.are.equal(pos[2], 15)
    end)
end)

describe("test B/E/W/gE", function()
    local Cursor = require "wordmotion.word".Cursor
    local cursor = Cursor:from_path(txt)
    it("tests B", function()
        local pos = cursor:get_position(-1, true, { 1, 7 })
        assert.are.equal(pos[1], 1)
        assert.are.equal(pos[2], 0)
    end)
    it("tests W", function()
        local pos = cursor:get_position(1, true, { 1, 7 })
        assert.are.equal(pos[1], 2)
        assert.are.equal(pos[2], 0)
    end)
end)
